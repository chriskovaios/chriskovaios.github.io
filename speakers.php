<u>Thorsten Strufe</u> is Professor for practical IT Security at KIT, Honorary Professor for Privacy and Network Security at TU Dresden, Deputy Spokesman of the Cluster of Excellence for Tactile Internet (CeTI, Centre for the Tactile Internet with Human-in-the-Loop) and PI of the Research Training Group RoSI and the 5G-Lab Germany. He is concerned with the questions of how to not only make web services and smart phone applications secure, but also how to protect the privacy of their users. To this end, his group measures and analyzes usage data on a large scale and develops algorithms and protocols both for decentralizing the systems and for securing the underlying networks and detecting anomalies at different levels (misconfigurations, attacks, fake news).
His research contributed, among other things, to uncovering and publicising data protection problems in social media (profile cloning, “Safebook”), simplifying privacy settings in social media (“Facebook Privacy Watcher”), investigating and reducing data protection problems in the use of printers (“DFD Printer Anonymisation”), as well as formally investigating anonymisation networks and making their actual protection against new attacks formally verifiable.
